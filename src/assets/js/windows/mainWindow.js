// â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
function createWindow() {
    // Assure qu'aucune autre instance n'est ouverte
    destroyWindow();

    mainWindow = new BrowserWindow({
        title: `${pkg.productName || 'Launcher'} ${pkg.version}`,
        width: 1280,
        height: 900,
        minWidth: 1280,
        minHeight: 900,
        resizable: true,
        icon: path.join(app.getAppPath(), `src/assets/images/icon.${os.platform() === 'win32' ? 'ico' : 'png'}`),
        frame: true,
        show: false, // plus fluide, on affiche aprÃ¨s "ready-to-show"
        backgroundColor: '#000000', // Ã©vite flash blanc Ã  lâ€™ouverture
        webPreferences: {
            contextIsolation: false,
            nodeIntegration: true,
            devTools: dev, // plus sÃ»r, Ã©vite dâ€™ouvrir devtools en prod
        },
    });

    // â•”â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—
    // â•‘ ğŸ—‚ï¸  WINDOW MENU CONFIGURATION                       â•‘
    // â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    Menu.setApplicationMenu(null);
    mainWindow.setMenuBarVisibility(false);

    // â•”â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—
    // â•‘ ğŸŒ  LOAD FRONTEND HTML                               â•‘
    // â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    const mainPath = path.join(app.getAppPath(), 'src/launcher.html');
    mainWindow.loadFile(mainPath).catch(err => {
        console.error('âŒ Failed to load launcher.html:', err);
    });

    // â•”â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—
    // â•‘ ğŸ””  SHOW WINDOW WHEN READY                            â•‘
    // â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    mainWindow.once('ready-to-show', () => {
        if (!mainWindow || mainWindow.isDestroyed()) return;
        if (dev) mainWindow.webContents.openDevTools({ mode: 'detach' });
        mainWindow.show();
    });

    // Gestion de fermeture propre
    mainWindow.on('closed', () => {
        mainWindow = undefined;
    });
}

