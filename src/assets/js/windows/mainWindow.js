function destroyWindow() {
    if (!mainWindow) return;
    try {
        if (!mainWindow.isDestroyed()) mainWindow.close();
    } catch (err) {
        console.warn('âš ï¸ Error while closing main window:', err);
    } finally {
        mainWindow = undefined;
        app.quit();
    }
}

